    public void verificar(){
        String q = s.getText();
        String w = i.getText();
        String r = l.getText();
        String t = L.getText();
        String y = a.getText();
        String silla = q + w + r + t + y;
        if(silla.equalsIgnoreCase("Silla")){
            s.setForeground(Color.GREEN);
            i.setForeground(Color.GREEN);
            l.setForeground(Color.GREEN);
            L.setForeground(Color.GREEN);
            a.setForeground(Color.GREEN);
            expresion.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resources/images/expressions/correcto.gif")));
        }
        else{
            s.setForeground(Color.RED);
            i.setForeground(Color.RED);
            l.setForeground(Color.RED);
            L.setForeground(Color.RED);
            a.setForeground(Color.RED);
            expresion.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resources/images/expressions/triste.gif")));
        }
    }
    
    public void reiniciar(){
        expresion.setIcon(new javax.swing.ImageIcon(getClass().getResource("/resources/images/expressions/expresion1.PNG")));
        s.setForeground(Color.BLACK);
        i.setForeground(Color.BLACK);
        l.setForeground(Color.BLACK);
        L.setForeground(Color.BLACK);
        a.setForeground(Color.BLACK);
        if( !("".equals(s.getText()))  &&  !("".equals(i.getText()))  && !("".equals(l.getText())) &&  !("".equals(L.getText()))  &&  !("".equals(a.getText()))){
            verificar();
        }
    }
    
