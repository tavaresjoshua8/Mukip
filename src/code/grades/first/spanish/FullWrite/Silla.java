    private void sKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sKeyReleased
        // TODO add your handling code here:
        if( evt.getKeyChar() == 'S' || evt.getKeyChar()=='s'){
            i.requestFocus();
        }
        if( back ){
            s.setText("S");
            i.setText("i");
            l.requestFocus();
            back=false;
        }
        reiniciar();
    }//GEN-LAST:event_sKeyReleased

